$(function() { 
    $('.menu_btn').click(function(){ 
        $('menu_ul').show('active');
        $('.menu_ul').toggleClass('active');
     });
 })
 
 $(function() { 
    $('.menu_close').click(function(){ 
        $('menu_ul').show('active');
        $('.menu_ul').toggleClass('active');
     });
 })